import assert from 'node:assert/strict'
import { readFile } from 'node:fs/promises'

const licenseManager = await readFile('src/main/license-manager.ts', 'utf8')
const ipcHandlers = await readFile('src/main/ipc-handlers.ts', 'utf8')
const rendererTypes = await readFile('src/renderer/src/types/index.ts', 'utf8')
const preloadTypes = await readFile('src/preload/index.d.ts', 'utf8')
const settingsPanel = await readFile('src/renderer/src/components/SettingsPanel.tsx', 'utf8')
const feedbackDialog = await readFile('src/renderer/src/components/FeedbackDialog.tsx', 'utf8')

assert.match(licenseManager, /POST/, 'Trial status should be fetched with POST')
assert.match(licenseManager, /\/api\/trial\/status/, 'Trial status should use the backend trial endpoint')
assert.match(licenseManager, /schemaVersion:\s*1/, 'Trial status request should include schemaVersion 1')
assert.match(licenseManager, /installId/, 'Trial status request should include installId')
assert.match(licenseManager, /appVersion:\s*app\.getVersion\(\)/, 'Trial status request should include appVersion')
assert.match(licenseManager, /source:\s*'app'/, 'Trial status request should identify the app source')
assert.match(licenseManager, /environment:\s*getTrialEnvironment\(\)/, 'Trial status request should include environment')
assert.match(licenseManager, /license\.backendTrialStatus/, 'Backend trial status should be cached for local fallback')
assert.match(licenseManager, /PULLFRAME_TRIAL_API_BASE_URL/, 'Trial API base should be overrideable independently')
assert.match(licenseManager, /https:\/\/pullframe\.app/, 'Default trial API base should match the deployed site endpoint')
assert.match(licenseManager, /PULLFRAME_BACKEND_TRIALS/, 'Backend trial checks should be manually enableable for dev smoke tests')
assert.match(licenseManager, /app\.isPackaged[\s\S]*MAIN_VITE_OFFICIAL_BUILD/, 'Official builds should use backend trial status')
assert.match(licenseManager, /const sourceBuildEntitlement: LicenseEntitlement/, 'Source-built apps should expose a synthetic unlocked entitlement')
assert.match(licenseManager, /isActivated: !isOfficialBuild \|\| entitlement !== null/, 'Source-built apps should function as activated without a stored license')
assert.doesNotMatch(licenseManager, /store\.set\('license\.entitlement', sourceBuildEntitlement\)/, 'Source-build activation should not persist an entitlement that survives an official update')
assert.match(licenseManager, /'active', 'expired', 'converted', 'voided'/, 'Trial status should accept the backend status values')
assert.match(licenseManager, /candidate\.trialDaysRemaining\s*>=\s*0/, 'Trial status should reject negative day counts')
assert.match(licenseManager, /body\.installId !== installId/, 'Trial status should reject mismatched installId responses')

assert.match(ipcHandlers, /refreshLicenseState\(persistedStore\)/, 'get-license-state IPC should refresh backend trial state')
assert.match(rendererTypes, /trialStatus: 'active' \| 'expired' \| 'converted' \| 'voided'/, 'Renderer LicenseState should expose trial status')
assert.match(rendererTypes, /trialSource: 'local' \| 'backend'/, 'Renderer LicenseState should expose trial source')
assert.match(preloadTypes, /trialStatus: 'active' \| 'expired' \| 'converted' \| 'voided'/, 'Preload LicenseState should expose trial status')
assert.match(preloadTypes, /trialSource: 'local' \| 'backend'/, 'Preload LicenseState should expose trial source')
assert.match(settingsPanel, /Trial unavailable/, 'Settings should display voided trials distinctly')
assert.match(feedbackDialog, /trialSource/, 'Feedback diagnostics should include local vs backend trial source')
